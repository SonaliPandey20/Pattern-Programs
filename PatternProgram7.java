public class PatternProgram7 {
    public static void main(String[] args) 
    {
        int l=1;
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=5;j++)
            {
                System.out.print(l + " ");
                l=l+2;
            }
            System.out.println();
        }
        
    }
}
