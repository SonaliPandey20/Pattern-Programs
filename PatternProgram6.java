public class PatternProgram6 
{
    public static void main(String[] args) 
    {
        int l=1;
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=5;j++)
            {
                System.out.print(l + " ");
                l++;
            }
            System.out.println();
        }
        
    }
}
